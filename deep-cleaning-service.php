                        <li><b>Floor Deep Cleaning:</b> This involves thorough cleaning of all types of flooring, including vacuuming, mopping, and steam cleaning carpets. Hardwood floors may also be polished or waxed.</li>
                        <li><b>Dusting and Polishing:</b> This covers dusting and polishing surfaces, furniture, baseboards, and trim throughout the home.</li>
                        <li><b>Window and Blind Cleaning:</b> This includes cleaning windows, frames, and tracks, as well as dusting and washing blinds or curtains.</li>
                        <li><b>Deep Cleaning of Upholstery:</b> This involves vacuuming and steam cleaning upholstery, such as sofas, chairs, and mattresses, to remove dirt, dust, and allergens.</li>
                        <li><b>Appliance Cleaning:</b> In addition to the kitchen appliances, deep cleaning services may also include cleaning other household appliances like washing machines, dryers, and dishwashers.</li>
                        <li><b>Organizational Deep Cleaning:</b> This involves decluttering and organizing spaces, such as closets, cabinets, and pantries, to maximize efficiency and cleanliness.
